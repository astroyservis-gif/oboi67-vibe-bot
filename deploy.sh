#!/bin/bash
set -e

echo "--- Начинаем обновление проекта через Docker ---"

# 1. Затягиваем изменения из Git (если используешь Git)
# git pull origin main 

# 2. Пересобираем и запускаем контейнеры
echo "--- Пересборка образов и запуск ---"
docker compose up -d --build

# 3. Очистка старых ненужных образов (чтобы не забивать место на сервере)
echo "--- Очистка мусора ---"
docker image prune -f

echo "--- Готово! Сайт и бот обновлены ---"
